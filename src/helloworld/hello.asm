* 6809
* Hello World
*
		NAM HELLO
		OPT l
		PAGE
		
VDU		EQU	$E030
		

VARS	EQU	$0F00
CURX	RMB	1
CURY	RMB	1
		
		ORG	$1000
		LDA	#0
		STA	CURX
		STA	CURY
		BSR	MOVECUR
		LDX	#MSG
		BSR	PRINTS
LOOP	JMP	LOOP

MOVECUR	LDY	#VDU
		LDA	CURX
		STA	2,Y
		LDA	CURY
		STA	3,Y
		RTS
	
PRINTS	LDY	#VDU
		LDA	,X+
		CMPA	#0
		BEQ	PRINTS1
		STA	,Y
		LDA	CURX
		INCA
		STA	2,Y
		STA	CURX
		JMP PRINTS
PRINTS1	RTS

MSG		FCC	'Hello World!'
		FCB	0
		END
